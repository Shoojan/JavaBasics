    public void getAllData(int id){
        
        EditPanel.setVisible(true);
        dbOperations.dbQueries dbQ = new dbOperations.dbQueries();
        rs = dbQ.VieweditStdInfo(id);
        try {
            if(rs.next()){
                this.EditTextname.setText(rs.getString("first_name"));
                this.EditTextlastname.setText(rs.getString("last_name"));
                this.EditTextaddress.setText(rs.getString("address"));
                this.EditTextphone.setText(rs.getString("phone"));
                this.EditTextgender.setText(rs.getString("gender"));
                this.EditTextemail.setText(rs.getString("email"));
                this.EditTextdob.setText(rs.getString("dob"));
            }
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(viewpanel.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
