    public void DisplayInitialValues(){
        dbOperations.dbQueries dbQ = new dbOperations.dbQueries();
        rs = dbQ.viewStdInfo();
        try {
            if(rs.next()){
                getData();
            }
        } catch (SQLException ex) {
            Logger.getLogger(viewpanel.getName()).log(Level.SEVERE, null, ex);
        }   
    }
    
    public void getData(){
        try {
            this.studentId.setText(rs.getString("id"));
            this.lblname.setText(rs.getString("first_name")+" "+rs.getString("last_name"));
            this.lbladdress.setText(rs.getString("address"));
            this.lblphone.setText(rs.getString("phone"));
            this.lblgender.setText(rs.getString("gender"));
            this.lblemail.setText(rs.getString("email"));
            this.lbldob.setText(rs.getString("dob"));
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"SQL Exception :"+ex.getMessage());
        }
                
    }
    
    

